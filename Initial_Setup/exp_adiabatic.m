%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% Adiabatic profile
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
clear all
clf

alpha = 3e-5; 
g     = 9.81; 
Cp_r  = 1050; 
Cp_r2  = 1040; 
Cp_b  = Cp_r2:100:2000; 
z     = 0:1:700;
z     = z.*1000; 
Cp = z.*0; 
Cp(z<410e3)=Cp_r; 
Cp(z>=410e3 & z< 660e3)= Cp_r2; 
for i = 1:length(Cp_b)
Cp(z>=660e3)=Cp_b(i);
T(i,:)    = 1350.*(1+(alpha.*g./Cp).*z);
end
figure(1)
plot(T,-z)
